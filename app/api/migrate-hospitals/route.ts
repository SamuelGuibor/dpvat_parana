function slugify(name: string): string {
  return name
    .toLowerCase()
    .normalize('NFD')
    .replace(/[̀-ͯ]/g, '')
    .replace(/[^a-z0-9]+/g, '_')
    .slice(0, 40);
}

export async function POST() {
  const session = await getServerSession(authOptions);
  if (!session?.user?.email) return NextResponse.json({ error: 'Não autenticado' }, { status: 401 });

  const me = await db.user.findUnique({ where: { email: session.user.email }, select: { role: true } });
  if (me?.role !== 'ADMIN++') return NextResponse.json({ error: 'Acesso negado' }, { status: 403 });

  // 1. Remove hospital from all users and processes
  const [clearedUsers, clearedProcesses] = await Promise.all([
    db.user.updateMany({ where: { hospital: { not: null }, role: { notIn: ['GHOST'] } }, data: { hospital: null } }),
    db.process.updateMany({ where: { hospital: { not: null } }, data: { hospital: null } }),
  ]);

  // 2. Delete existing ghost users to avoid duplicates
  await db.user.deleteMany({ where: { role: 'GHOST' } });

  // 3. Create ghost users for each hospital using a sequence for cardNumber
  let created = 0;
  for (let i = 0; i < HOSPITALS.length; i++) {
    const name = HOSPITALS[i];
    const slug = slugify(name);
    const email = `ghost_hosp_${slug}@hospital.internal`;

    const rows = await db.$queryRawUnsafe<{ nextval: bigint }[]>(`SELECT nextval('card_number_seq') AS nextval`);
    const cardNumber = Number(rows[0].nextval);

    await db.user.upsert({
      where: { email },
      update: { hospital: name },
      create: {
        name: `[HOSPITAL] ${name}`,
        email,
        password: 'ghost_user_no_login',
        role: 'GHOST',
        hospital: name,
        cardNumber,
      },
    });
    created++;
  }

  return NextResponse.json({
    ok: true,
    clearedUsers: clearedUsers.count,
    clearedProcesses: clearedProcesses.count,
    ghostsCreated: created,
  });
}
